ESTADOCLI VARCHAR(100),
CEPCLI VARCHAR(100),
RGCLI VARCHAR(100),
CPFCLI VARCHAR(100),
DATANASCIMENTOCLI DATETIME,
TELEFONECLI VARCHAR(15),
SEXOCLI VARCHAR(10),
EMAILCLI VARCHAR(100));

